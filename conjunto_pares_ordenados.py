class ConjuntoParesOrdenados:

  def __init__(self, conjunto1: list, conjunto2: list):
    self.c1 = conjunto1
    self.c2 = conjunto2


#get's e set's

  def get_c1(self):
    return self.c1

  def get_c2(self):
    return self.c2

  #set's

  def set_c1(self, c1):
    self.c1 = c1

  def setc2(self, c2):
    self.c2 = c2


